package model;

public class Data {

    private String head;
    private int id;
    private String note;

    public void setHead(String head) {
        this.head = head;
    }

    public String getHead() {
        return head;
    }



    public void setId(int id) {
        this.id = id;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public String getNote() {
        return note;
    }
}
